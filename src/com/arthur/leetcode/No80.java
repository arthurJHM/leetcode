    public int removeDuplicates(int[] nums) {
        int n = 0;
        int len = nums.length;
        for (int i = 0; i < len; i++) {
            if (n < 2 || nums[i] != nums[n - 2]) {
                nums[n++] = nums[i];
            }
        }
        return n;
    }
