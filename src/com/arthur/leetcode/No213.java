    public int rob(int[] nums) {
        int len = nums.length;
        if(len == 0) {
            return 0;
        }
        if(len == 1) {
            return nums[0];
        }
        int a = rob1(Arrays.copyOfRange(nums, 0, len - 1));
        int b = rob1(Arrays.copyOfRange(nums, 1, len));
        return Math.max(a, b);

    }

    private int rob1(int[] nums) {
        int len = nums.length;
        if(len == 1) {
            return nums[0];
        }
        int[] dp = new int[len];
        dp[0] = nums[0];
        dp[1] = Math.max(nums[0], nums[1]);
        for (int i = 2; i < len; i++) {
            dp[i] = Math.max(dp[i - 1], dp[i - 2] + nums[i]);
        }
        return dp[len - 1];
    }
